package com.variables;

public class LocalVariables {

	public static void main(String[] args) {
		int num=10;
		System.out.println("local variable:" +num);
		byte b=127;
		System.out.println("local variable:" +b);
		short s= 2;
		System.out.println("local variable:" +s);
		long l= 100000L;
		System.out.println("local variable:" +l);
		float f=10.63f;
		System.out.println("local variable:" +f);
		double d= 12.22;
		System.out.println("local variable:" +d);
		String firstName="Nikita";
		String LastName= "Jha";
		int age=24;
		System.out.println(firstName + " " +LastName);
		System.out.println("My age is :" +age);
		

	}

}
