package com.operators;

public class Shift {

	public static void main(String[] args) {
		int a = 10;
		int b = a >> 2; // 2
		System.out.println(b);
		int c = 05;
		int d = c <<2 ; //20
		System.out.println(d);//20
		
		int e = 04;
		int f = e>>3;
		System.out.println(f);//0
		int x = 11;
		int y= x <<3;
		System.out.println(y);
		int w = 16;
		int q = w <<1;
		System.out.println(q);
		
	}

}
